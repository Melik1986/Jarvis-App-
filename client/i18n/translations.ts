const privacyPolicyEn = `üõ° SECURITY & PRIVACY ARCHITECTURE
Project AURA (formerly JSRVIS) is based on the Zero-Knowledge Architecture principle. We do not store, transmit, or have access to users' secret keys or business data. The application acts as a "conductor", providing a secure communication channel between the local device and the selected infrastructure.

1. Hardware-Backed Security
All confidential data (LLM provider API keys, database credentials) are protected at the hardware level:

iOS: Using Keychain Services with encryption inside Secure Enclave.

Android: Using Hardware-backed Keystore (StrongBox/TEE). Data is encrypted with a key that never leaves the device's secure chip. Even with Root privileges, an attacker cannot extract keys in plain text.

2. Human-in-the-Loop Principle
To prevent unauthorized changes in accounting systems (e.g., 1C), a two-factor transaction confirmation mechanism is implemented:

The AI agent only forms a proposal (Draft) in JSON format.

Data is displayed to the user for verification.

Writing to the database is initiated only after the user physically presses the "Confirm" button.

3. Biometric Authentication
Access to provider management screens and sensitive settings is protected by the device's system biometric APIs (FaceID / TouchID / Biometric Prompt). This prevents access to API keys even if the unlocked device is stolen.

4. Network Security and TLS
All requests to external providers (OpenAI, Groq, Anthropic) and databases are executed exclusively via HTTPS/TLS 1.3 protocol. The application blocks any connection attempts over insecure channels, eliminating Man-in-the-Middle (MITM) attacks.

5. No Telemetry
The backend part of the application (NestJS) in the "Conductor" model does not log the content of requests and keys. We collect only anonymous technical statistics to ensure service operability, without touching the user's business logic.`;

